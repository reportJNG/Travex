const I18nContext = createContext<{
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
  formatCurrency: (value: number, currency?: string) => string;
  formatDate: (value: string | Date, options?: Intl.DateTimeFormatOptions) => string;
  dir: "ltr" | "rtl";
}>({
  locale: "fr",
  setLocale: () => {},
  t: (key: string) => key,
  formatCurrency: value => `${value}`,
  formatDate: value => String(value),
  dir: "ltr",
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("travex-locale");
    return (saved as Locale) || "fr";
  });

  const setLocale = useCallback((l: Locale) => {
    localStorage.setItem("travex-locale", l);
    setLocaleState(l);
    document.documentElement.dir = l === "ar" ? "rtl" : "ltr";
    document.documentElement.lang = l;
  }, []);

  const t = useCallback(
    (key: string) => {
      return translations[locale]?.[key] || key;
    },
    [locale]
  );

  const dir = locale === "ar" ? "rtl" : "ltr";
  const intlLocale = locale === "ar" ? "ar-DZ" : locale === "en" ? "en-GB" : "fr-DZ";

  const formatCurrency = useCallback(
    (value: number, currency = "DZD") =>
      new Intl.NumberFormat(intlLocale, {
        style: "currency",
        currency,
        maximumFractionDigits: 0,
      }).format(value),
    [intlLocale],
  );

  const formatDate = useCallback(
    (value: string | Date, options?: Intl.DateTimeFormatOptions) =>
      new Intl.DateTimeFormat(intlLocale, options ?? { dateStyle: "medium" }).format(new Date(value)),
    [intlLocale],
  );

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, formatCurrency, formatDate, dir }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
